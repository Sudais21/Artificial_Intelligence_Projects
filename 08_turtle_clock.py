import turtle
turtle.color("magenta")
turtle.circle(100)
turtle.goto(0,0)
turtle.write(6)

turtle.penup()
turtle.goto(0,185)
turtle.write(12)
turtle.pendown()

turtle.right(90)
turtle.forward(80)

turtle.right(90)
turtle.forward(90)
turtle.write(9)

turtle.left(180)
turtle.penup()
turtle.goto(0,105)
turtle.pendown()
turtle.forward(90)
turtle.write(3)


turtle.done()