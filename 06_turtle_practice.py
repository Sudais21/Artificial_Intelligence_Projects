import turtle
turtle.color("blue")
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)

turtle.color("red")
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)

turtle.penup()
turtle.goto(-0,100)
turtle.right(90)
turtle.pendown()
turtle.color("green")
turtle.forward(100)
turtle.right(-90)
turtle.forward(100)
turtle.right(-90)
turtle.forward(100)

turtle.penup()
turtle.goto(-0,-100)
turtle.left(180)
turtle.color("black")
turtle.pendown()
turtle.forward(100)
turtle.right(90)
turtle.forward(100)

turtle.done()